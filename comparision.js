


// Comparision Operator 

var a = 23;
var b = 21;

document.write( a > b );   // true
document.write("<br>");    
document.write( a < b );   // false 
document.write("<br>");
document.write( a == b);   // false
document.write("<br>"); 
document.write( a === b ); // false
document.write("<br>"); 
document.write( a >= b);   // true
document.write("<br>"); 
document.write( a <= b );  // false
document.write("<br>");
document.write( a != b);   //true